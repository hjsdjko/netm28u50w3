							this.ruleForm.rukushijian = obj[o];
							this.ro.rukushijian = true;
							continue;
						}
						if(o=='caigougonghao'){
							this.ruleForm.caigougonghao = obj[o];
							this.ro.caigougonghao = true;
							continue;
