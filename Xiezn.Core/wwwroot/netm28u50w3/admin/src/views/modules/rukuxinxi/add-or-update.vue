				method: "get"
			}).then(({ data }) => {
				if (data && data.code === 0) {
					var json = data.data;
					if(((json.caigougonghao!=''&&json.caigougonghao) || json.caigougonghao==0) && this.$storage.get("role")!="管理员"){
						this.ruleForm.caigougonghao = json.caigougonghao
						this.ro.caigougonghao = true;
